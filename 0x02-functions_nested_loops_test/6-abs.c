#include <stdio.h>
#include "main.h"

/**
 * _abs - computes the absolute value of an integer
 * @num: integer to compute absolute value of
 *
 * Return: 0
 */
int _abs(int num)
{
	if (num < 0)
	{
		return (-num);
	}
	else if (num == 0)
	{
		return (num);
	}
	else
	{
		return (num);
	}
}
